animal = " Pig "
print(animal.upper())  # PIG
print(animal.lower())  # pig
print(animal.strip().capitalize())  # Pig
print(animal.title())  # Pig
print(animal.strip())  # pig
print(animal.lstrip())  # Pig
print(animal.rstrip())  # Pig
print(animal.find("i"))  # 2
print(animal.replace("Pig", "Dog"))  # Dog
print("i" in animal)  # True
print("i" not in animal)  # False
